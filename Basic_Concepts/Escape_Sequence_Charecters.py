#print the output with a tab space
print("Hello\tWorld")

#print the output in Double quotes
print("\"Hello World\"")

#Print the output in single Quotes
print("\'Hello World\'")

#Print the output in back slashes
print("\\Hello World\\")

#Print the output by removing one trailing space
print("Hello  \bWorld")

#print the output by overriding trailing charecters
print("Hello\rWorld")

#print the output by using vertical tab
print("Hello\vWorld")

#print the output as same as\v
print("Hello\fWorld")

#it prints nothing null charecter
print("Hello\0World")

