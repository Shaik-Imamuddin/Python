#Identifier is a name used to identify a variable, function, class, module, etc.
#The identifier is a combination of character digits and underscore.
#The identifier should start with a character or Underscore then use a digit.
#The characters are A-Z or a-z, an Underscore ( _ ) , and digit (0-9).
'Rules for Naming an Identifier:'
#*Identifiers cannot be a keyword.
#*Identifiers are case-sensitive.
#*It can have a sequence of letters and digits.
#*However, it must begin with a letter or _.
#*The first letter of an identifier cannot be a digit.
#*It's a convention to start an identifier with a letter rather _.
#*Whitespaces are not allowed.
#*We cannot use special symbols like !, @, #, $, and so on.
a=10
print(a)
b='python'
print(b)


